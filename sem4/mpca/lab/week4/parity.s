	MOV R0, #0x000000F4
	MOV R1, #32
	MOV R2, #0
	MOV R3, #0
	MOV R4, #0

LOOP:
	AND R4, R0, #1
	CMP R4, #1
	BEQ COUNT
	ADD R3, R3, #1
	B   SHIFT

COUNT:
	ADD R2, R2, #1

SHIFT:
	MOV R0, R0, LSR #1
	SUB R1, R1, #1
	CMP R1, #0
	BNE LOOP
	AND R2, R2, #1
	CMP R2, #0
	BEQ EVEN
	MOV R0, #0x00000001
	B   END

EVEN:
	MOV R0, #0x00000000

END:
	SWI 0x011
